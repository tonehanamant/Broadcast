CREATE PROCEDURE [dbo].[usp_STS2_selectMinimumDmaDate]
	@dma_id int
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

	SELECT MIN(start_date) FROM uvw_dma_universe (NOLOCK) WHERE dma_id=@dma_id
END
