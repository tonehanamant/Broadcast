CREATE TABLE [dbo].[activities] (
    [id]                 INT      IDENTITY (1, 1) NOT NULL,
    [contact_id]         INT      NOT NULL,
    [activity_type_id]   INT      NOT NULL,
    [employee_id]        INT      NULL,
    [comment]            TEXT     NOT NULL,
    [date_created]       DATETIME NOT NULL,
    [date_last_modified] DATETIME NOT NULL,
    CONSTRAINT [PK_activities] PRIMARY KEY CLUSTERED ([id] ASC),
    CONSTRAINT [FK_activities_activity_types] FOREIGN KEY ([activity_type_id]) REFERENCES [dbo].[activity_types] ([id]),
    CONSTRAINT [FK_activities_contacts] FOREIGN KEY ([contact_id]) REFERENCES [dbo].[contacts] ([id]),
    CONSTRAINT [FK_activities_employees] FOREIGN KEY ([employee_id]) REFERENCES [dbo].[employees] ([id])
);


