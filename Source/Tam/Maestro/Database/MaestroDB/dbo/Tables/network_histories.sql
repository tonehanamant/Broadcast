CREATE TABLE [dbo].[network_histories] (
    [network_id]            INT          NOT NULL,
    [start_date]            DATETIME     NOT NULL,
    [code]                  VARCHAR (15) NOT NULL,
    [name]                  VARCHAR (63) NOT NULL,
    [active]                BIT          NOT NULL,
    [flag]                  TINYINT      NULL,
    [end_date]              DATETIME     NOT NULL,
    [language_id]           TINYINT      DEFAULT ((1)) NOT NULL,
    [affiliated_network_id] INT          NULL,
    [network_type_id]       TINYINT      NULL,
    CONSTRAINT [PK_network_histories] PRIMARY KEY CLUSTERED ([network_id] ASC, [start_date] ASC),
    CONSTRAINT [FK_network_histories_languages] FOREIGN KEY ([language_id]) REFERENCES [dbo].[languages] ([id]),
    CONSTRAINT [FK_network_histories_network] FOREIGN KEY ([affiliated_network_id]) REFERENCES [dbo].[networks] ([id]),
    CONSTRAINT [FK_network_histories_networks] FOREIGN KEY ([network_id]) REFERENCES [dbo].[networks] ([id]),
    CONSTRAINT [FK_network_types_network_histories] FOREIGN KEY ([network_type_id]) REFERENCES [dbo].[network_types] ([id])
