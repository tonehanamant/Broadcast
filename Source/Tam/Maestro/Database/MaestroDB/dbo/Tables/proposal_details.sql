CREATE TABLE [dbo].[proposal_details] (
    [id]                       INT        IDENTITY (1, 1) NOT NULL,
    [proposal_id]              INT        NOT NULL,
    [spot_length_id]           INT        NOT NULL,
    [network_id]               INT        NOT NULL,
    [daypart_id]               INT        NOT NULL,
    [ratings_daypart_id]       INT        NULL,
    [num_spots]                INT        NOT NULL,
    [proposal_rate]            MONEY      NULL,
    [rate_card_rate]           MONEY      NULL,
    [start_date]               DATETIME   NOT NULL,
    [end_date]                 DATETIME   NOT NULL,
    [topography_universe]      FLOAT (53) NULL,
    [universal_scaling_factor] FLOAT (53) NULL,
    [include]                  BIT        NOT NULL,
    [date_created]             DATETIME   NOT NULL,
    [date_last_modified]       DATETIME   NOT NULL,
    CONSTRAINT [PK_proposal_details] PRIMARY KEY CLUSTERED ([id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_proposal_details_dayparts] FOREIGN KEY ([daypart_id]) REFERENCES [dbo].[dayparts] ([id]),
    CONSTRAINT [FK_proposal_details_dayparts1] FOREIGN KEY ([ratings_daypart_id]) REFERENCES [dbo].[dayparts] ([id]),
    CONSTRAINT [FK_proposal_details_networks] FOREIGN KEY ([network_id]) REFERENCES [dbo].[networks] ([id]),
    CONSTRAINT [FK_proposal_details_proposals] FOREIGN KEY ([proposal_id]) REFERENCES [dbo].[proposals] ([id]),
    CONSTRAINT [FK_proposal_details_spot_lengths] FOREIGN KEY ([spot_length_id]) REFERENCES [dbo].[spot_lengths] ([id])
);


GO
CREATE NONCLUSTERED INDEX [IX_proposal_details_proposal]
    ON [dbo].[proposal_details]([proposal_id] ASC)
    INCLUDE([id], [network_id]) WITH (FILLFACTOR = 90);


