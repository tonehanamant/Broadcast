CREATE TABLE [dbo].[zone_histories] (
    [zone_id]                       INT          NOT NULL,
    [start_date]                    DATETIME     NOT NULL,
    [code]                          VARCHAR (15) NOT NULL,
    [name]                          VARCHAR (63) NOT NULL,
    [type]                          VARCHAR (63) CONSTRAINT [DF_zone_histories_type] DEFAULT ('N/A') NOT NULL,
    [primary]                       BIT          NOT NULL,
    [traffic]                       BIT          NOT NULL,
    [dma]                           BIT          NOT NULL,
    [flag]                          TINYINT      NULL,
    [active]                        BIT          NOT NULL,
    [end_date]                      DATETIME     NOT NULL,
    [time_zone_id]                  INT          NULL,
    [observe_daylight_savings_time] BIT          DEFAULT ((0)) NOT NULL,
    CONSTRAINT [PK_zone_histories] PRIMARY KEY CLUSTERED ([zone_id] ASC, [start_date] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_zone_histories_time_zones] FOREIGN KEY ([time_zone_id]) REFERENCES [dbo].[time_zones] ([id]),
    CONSTRAINT [FK_zone_histories_zones] FOREIGN KEY ([zone_id]) REFERENCES [dbo].[zones] ([id])
);




