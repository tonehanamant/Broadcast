CREATE TABLE [dbo].[contacts] (
    [id]                 INT          IDENTITY (1, 1) NOT NULL,
    [company_id]         INT          NOT NULL,
    [salutation_id]      INT          NULL,
    [first_name]         VARCHAR (63) NOT NULL,
    [last_name]          VARCHAR (63) NOT NULL,
    [title]              VARCHAR (63) NOT NULL,
    [department]         VARCHAR (63) NOT NULL,
    [assistant]          VARCHAR (63) NOT NULL,
    [assistant_title]    VARCHAR (63) NOT NULL,
    [date_created]       DATETIME     NOT NULL,
    [date_last_modified] DATETIME     NOT NULL,
    CONSTRAINT [PK_contacts] PRIMARY KEY CLUSTERED ([id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_contacts_companies] FOREIGN KEY ([company_id]) REFERENCES [dbo].[companies] ([id]),
    CONSTRAINT [FK_contacts_salutations] FOREIGN KEY ([salutation_id]) REFERENCES [dbo].[salutations] ([id])
);


