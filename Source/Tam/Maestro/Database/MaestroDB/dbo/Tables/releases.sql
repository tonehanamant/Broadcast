CREATE TABLE [dbo].[releases] (
    [id]              INT            IDENTITY (1, 1) NOT NULL,
    [category_id]     INT            NOT NULL,
    [status_id]       INT            NOT NULL,
    [name]            VARCHAR (63)   NOT NULL,
    [description]     VARCHAR (63)   NOT NULL,
    [notes]           VARCHAR (2047) NOT NULL,
    [release_date]    DATETIME       NOT NULL,
    [confirm_by_date] DATETIME       NOT NULL,
    CONSTRAINT [PK_releases] PRIMARY KEY CLUSTERED ([id] ASC),
    CONSTRAINT [FK_release_2_category] FOREIGN KEY ([category_id]) REFERENCES [dbo].[categories] ([id]),
    CONSTRAINT [FK_release_2_status] FOREIGN KEY ([status_id]) REFERENCES [dbo].[statuses] ([id])
);


