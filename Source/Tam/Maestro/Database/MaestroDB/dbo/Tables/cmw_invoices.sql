CREATE TABLE [dbo].[cmw_invoices] (
    [id]                      INT          IDENTITY (1, 1) NOT NULL,
    [media_month_id]          INT          NOT NULL,
    [status_code]             TINYINT      NOT NULL,
    [external_invoice_number] VARCHAR (63) NOT NULL,
    [gross_due]               MONEY        NOT NULL,
    [agency_commission_fee]   MONEY        NOT NULL,
    [net_due_to_rep]          MONEY        NOT NULL,
    [rep_commission_fee]      MONEY        NOT NULL,
    [net_due_to_network]      MONEY        NOT NULL,
    [created_by_employee_id]  INT          NOT NULL,
    [modified_by_employee_id] INT          NULL,
    [date_created]            DATETIME     NOT NULL,
    [date_last_modified]      DATETIME     NULL,
    CONSTRAINT [PK_cmw_invoices] PRIMARY KEY CLUSTERED ([id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_cmw_invoices_employees] FOREIGN KEY ([created_by_employee_id]) REFERENCES [dbo].[employees] ([id]),
    CONSTRAINT [FK_cmw_invoices_employees1] FOREIGN KEY ([modified_by_employee_id]) REFERENCES [dbo].[employees] ([id]),
    CONSTRAINT [FK_cmw_invoices_media_months] FOREIGN KEY ([media_month_id]) REFERENCES [dbo].[media_months] ([id])
