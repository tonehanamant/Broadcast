CREATE TABLE [dbo].[campaigns] (
    [id]                 INT            IDENTITY (1, 1) NOT NULL,
    [name]               NVARCHAR (127) NULL,
    [company_id]         INT            NOT NULL,
    [date_created]       DATETIME       NOT NULL,
    [date_last_modified] DATETIME       NOT NULL,
    [budget]             MONEY          NULL,
    [agency_id]          INT            NULL,
    [version]            INT            DEFAULT ((1)) NOT NULL,
    [last_modified_by]   INT            NULL,
    [start_date]         DATETIME       NULL,
    [end_date]           DATETIME       NULL,
    CONSTRAINT [PK_campaigns] PRIMARY KEY CLUSTERED ([id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [FK_campaigns_companies] FOREIGN KEY ([company_id]) REFERENCES [dbo].[companies] ([id]),
    CONSTRAINT [FK_campaigns_companies1] FOREIGN KEY ([agency_id]) REFERENCES [dbo].[companies] ([id])
